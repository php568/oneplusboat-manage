                if(i >= video_list.length){
                    i = 0;
                }
                var videoObj = video_list[i];
                this.src({type: videoObj.type, src: videoObj.url});
                this.play();

                $(".video-img").removeClass('current');
                $("#video"+i+" img").addClass('current');
            })
        });

